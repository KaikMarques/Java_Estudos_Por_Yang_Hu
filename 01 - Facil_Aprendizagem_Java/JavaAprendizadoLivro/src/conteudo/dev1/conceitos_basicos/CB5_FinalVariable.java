package conteudo.dev1.conceitos_basicos;

public class CB5_FinalVariable {

    public static void main(String[] args) {

        //  Constante (Constant): o valor n√£o pode ser alterado.

        //Integer Constante
        final int UP = 0;
        final int DOWN = 1;
        System.out.println(UP);
        System.out.println(DOWN);

        //Floating Constante
        final float PI = 3.14f;
        System.out.println(PI);

        //Boolean Constante
        final boolean RUN = true;
        System.out.println(RUN);

        //Char Constante
        final char A = 'A';

        //String Constante
        final String CONST = "love for forever";
        System.out.println(CONST);

    }
}
