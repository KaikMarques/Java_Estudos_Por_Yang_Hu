package conteudo.dev1.conceitos_basicos;

/*
    Variável (Variable): uma área de memória alocada pelo sistema para armazenar dados.
*/

public class CB2_Variable {
    public static void main(String[] args) {
        String basket = "Apple";
        System.out.println(basket);

        basket = "Orange";
        System.out.println(basket);
    }
}
